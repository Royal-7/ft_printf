#include "libft.h"

char	*ft_strncat(char *str1, char const *str2, size_t n)
{
	int	x;
	int	y;

	x = 0;
	y = 0;
	while (str1[x] != '\0')
		x++;
	while (str2[y] != '\0' && y < (int)n)
	{
		str1[x + y] = str2[y];
		y++;
	}
	str1[x + y] = '\0';
	return (str1);
}
