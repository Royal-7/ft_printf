#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int		x;

	x = 0;
	while (str[x] != '\0')
	{
		if (str[x] == c)
			return ((char *)&str[x]);
		x++;
	}
	if (c == '\0')
		return ((char *)&str[x]);
	return (NULL);
}
