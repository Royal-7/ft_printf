char	*ft_strcat(char *str1, const char *str2)
{
	int	x;
	int	y;

	x = 0;
	y = 0;
	while (str1[x] != '\0')
		x++;
	while (str2[y] != '\0')
	{
		str1[x + y] = str2[y];
		y++;
	}
	str1[x + y] = '\0';
	return (str1);
}
